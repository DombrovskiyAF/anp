uint32_t crc32_aal5( const uint8_t* buf, size_type len, uint32_t crc_in )
{
  uint32_t result = crc_in;

  while( len-- != 0 )
    result = crc32_aal5_table[ (result >> 24) ^ *buf++ ] ^ (result << 8);

  return ~result;
}

